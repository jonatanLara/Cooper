    public void combo(){
        conexion.conectar();
        Statement sentencia = conexion.getStatement();
        ComboNivelUsuario.setModel(new javax.swing.DefaultComboBoxModel(
                new String[] {}));
        try {
             ResultSet r = sentencia.executeQuery("select * from nivel");
             r.beforeFirst();
             while (r.next()) {
                 String item = r.getString("nivel");
                 ComboNivelUsuario.addItem(new String(item));
             }
             conexion.Desconectar();
             
        } catch (Exception e) {
        }
    }
