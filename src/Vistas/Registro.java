    public void insetarPersona(String nombre,String apellido,String idbanner,String correo,String usuario, String contrasena,int nivel){
        try {
        Connection reg = conexion.conctarDB();
        String sql = "INSERT INTO persona "
                        + "(id_persona,nombre,apellido,correo,id_banner,creado)"
                        + "VALUES (NULL,?,?,?,?,?)";
        PreparedStatement pst = reg.prepareStatement(sql);
                pst.setString(1, nombre);
                pst.setString(2, apellido);
                pst.setString(3, correo);
                pst.setString(4, idbanner);
                pst.setString(5, fecha);
            int n = pst.executeUpdate();
            if (n>0) {
                JOptionPane.showMessageDialog(null,"Se han insertado de manera Exitosa");
            }
        } catch (SQLException | HeadlessException e) {
        }
        
    }
