    public boolean validar(){
        boolean result = false;
        String nombre =txtNombre.getText(),apellido=txtApellido.getText(),idbanner=txtIDBanner.getText(),
                correo=txtCorreo.getText(),usuario=txtUsuario.getText(),contrasena= txtContrasena.getText();
        
        if (nombre.equals("")||apellido.endsWith("")||idbanner.equals("")||correo.equals("")||usuario.equals("")
               ||contrasena.equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor llena todos los datos solicitados");
            result= false;
        }else{
            result = true;
        }
        return result;
    }
    
