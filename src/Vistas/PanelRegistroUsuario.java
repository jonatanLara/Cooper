  private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
    if (nameEt.getText().length() == 0
            || lastNameEt.getText().length() == 0
            || emailEt.getText().length() == 0
            || bannerEt.getText().length() == 0
            || userEt.getText().length() == 0
            || passwordEt.getText().length() == 0) {
      JOptionPane.showMessageDialog(this, "Debes llenar todos los campos correctamente");
    } else {
      try {
        Persona persona = new Persona(
                nameEt.getText().trim(),
                lastNameEt.getText().trim(),
                emailEt.getText().trim(),
                bannerEt.getText().trim(),
                LocalDate.now()
        );

        DatabaseConnection databaseConnection = new DatabaseConnection();
        long idPersona = persona.crearPersona(databaseConnection);
        ArrayList<Nivel> niveles = new Nivel().obtenerNiveles(databaseConnection);
        int nivelSeleccionado = 0;
        for(Nivel nivel: niveles) {
           if (levelCb.getSelectedItem().toString().equals(nivel.getNivel().toString())) {
            nivelSeleccionado = nivel.getNivel();
          }
        }
        if (idPersona != 0) {
          Usuario usuario = new Usuario(
                  userEt.getText().trim(),
                  passwordEt.getText().trim(),
                  nivelSeleccionado,
                  Integer.parseInt(String.valueOf(idPersona)),
                  LocalDate.now()
          );
           
          long idUsuario = usuario.crearUsuario(databaseConnection);
          if (idUsuario != 0) {
            JOptionPane.showMessageDialog(this, "Usuario creado correctamente");
          } else {
            JOptionPane.showMessageDialog(this, "Error guardando usuario");
          }
        } else {
          JOptionPane.showMessageDialog(this, "Error guardando persona");
        }
      } catch (SQLException | ClassNotFoundException ex) {
        Logger.getLogger(PanelRegistroUsuario.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Error");
      }
    }
  }//GEN-LAST:event_registerBtnActionPerformed

  private void setUp() {
    try {
      Nivel nivelObj = new Nivel();
      try {
        ArrayList<Nivel> niveles = nivelObj.obtenerNiveles(new DatabaseConnection());
        if (niveles != null && niveles.size() > 0) {
          niveles.forEach((nivel) -> {
              System.out.println("tamano "+niveles.size());
            switch (nivel.getNivel()) {
              case 1:
                //levelCb.addItem("Administrativo");
                modeloNivel.addElement("Administrativo");
                break;
              case 2:
                //levelCb.addItem("Coordinador");
                modeloNivel.addElement("Coordinador");
                break;
              case 3:
               // levelCb.addItem("Estudiante");
                modeloNivel.addElement("Estudiante");
                break;
            }
            levelCb.setModel(modeloNivel);
          });
        }
      } catch (SQLException ex) {
        Logger.getLogger(PanelRegistroUsuario.class.getName()).log(Level.SEVERE, null, ex);
      }
    } catch (ClassNotFoundException ex) {
      Logger.getLogger(PanelRegistroUsuario.class.getName()).log(Level.SEVERE, null, ex);
    }
  }


